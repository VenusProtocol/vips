// Function to filter assets based on a base asset
const filterAssets = (assets: string[], baseAsset: string, whitelistedAssets: string[]) =>
  assets.filter(
    asset =>
      asset.toLowerCase() !== baseAsset.toLowerCase() &&
      !whitelistedAssets.map(asset => asset.toLowerCase()).includes(asset.toLowerCase()),
  );

